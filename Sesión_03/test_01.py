"""Listas en Python"""

"""Listas -> pop(): Quita un elemento de una posición dada"""

paises=["Perú","Canada","Argentina","Mexico", "España"]

print("Mi lista inicia:{}".format(paises))
print(paises)

paises.pop()

print("Mi lista actualizada es:{}".format(paises))

print("paises[2]:{}".format(paises[2]))
paises.pop(2)
print("Mi lista inicia:{}".format(paises))

paises.pop(2)
print("Mi lista inicia:{}".format(paises))

